#'   \item{ME_per}{A numeric vector representing mental performance}
#' }
#' @source <https://doi.org/doi:10.18112/openneuro.ds004215.v1.0.2>
#' @references Nugent, A. C., Thomas, A. G., Mahoney, M., Gibbons, A., Smith, J. T., Charles, A. J., ... & Chung, J. (2022). The NIMH intramural healthy volunteer dataset: A comprehensive MEG, MRI, and behavioral resource. \emph{Scientific Data}, 9(1), 518.
